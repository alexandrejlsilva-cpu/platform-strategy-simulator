const SK = '#F4C2A1'; // skin

export function detectArchetype(player) {
  if (!player) return 'DEFAULT';
  const t = `${player.name} ${player.role} ${player.supply} ${player.demand}`.toLowerCase();
  if (/investor|venture|vc|capital|fund|angel|backer/.test(t))        return 'INVESTOR';
  if (/advertis|brand|sponsor|media buyer|marketing/.test(t))          return 'ADVERTISER';
  if (/buy|consumer|shopper|customer|client/.test(t))                   return 'BUYER';
  if (/sell|merchant|vendor|retailer|trader/.test(t))                   return 'SELLER';
  if (/creat|design|artist|content|writer|music|photo/.test(t))         return 'CREATOR';
  if (/tech|dev|engineer|software|coder|data|ai/.test(t))               return 'TECH';
  if (/service|driver|deliver|worker|provider|host|contractor/.test(t)) return 'SERVICE';
  return 'DEFAULT';
}

