// ─── Component ────────────────────────────────────────────────────────────────

export default function PixelAvatar({ player, scale = 3, showBack = false }) {
  const COLS = 14, ROWS = 22;
  const W = COLS * scale, H = ROWS * scale;
  const archetype = showBack ? 'BACK' : detectArchetype(player);
  const pixels = ARCHETYPES[archetype] || ARCHETYPES.DEFAULT;

  return (
    <svg
      width={W} height={H}
      viewBox={`0 0 ${W} ${H}`}
      style={{ imageRendering: 'pixelated', display: 'block', flexShrink: 0 }}
    >
      {pixels
        .filter(([c, r]) => c >= 0 && r >= 0 && c < COLS && r < ROWS)
        .map(([c, r, color, w = 1, h = 1], i) => (
          <rect key={i} x={c * scale} y={r * scale} width={w * scale} height={h * scale} fill={color} />
        ))}
    </svg>
  );
}
